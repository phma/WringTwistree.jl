function testVectorWring(wring,plaintext,ciphertext)
  plaintext=Vector{UInt8}(plaintext)
  text=copy(plaintext)
  encrypt!(wring,text)
  ret=text==ciphertext
  if !ret
    println("Expected ciphertext: ",ciphertext,"\nGot: ",text)
  end
  decrypt!(wring,text)
  ret&=text==plaintext
  ret
end

